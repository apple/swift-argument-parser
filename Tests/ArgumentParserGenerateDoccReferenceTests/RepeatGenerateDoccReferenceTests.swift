import XCTest
import ArgumentParserTestHelpers

final class RepeatGenerateDoccReferenceTests: XCTestCase {
  func testMath() throws {
    try AssertGenerateDoccReference(command: "repeat", expected: #"""
      # repeat

      <!-- Generated by swift-argument-parser -->

      ```
      repeat [--count=<count>] [--include-counter] <phrase> [--help]
      ```

      **--count=\<count\>:**

      *The number of times to repeat 'phrase'.*


      **--include-counter:**

      *Include a counter with each repetition.*


      **phrase:**

      *The phrase to repeat.*


      **--help:**

      *Show help information.*
      """#)
  }
}
